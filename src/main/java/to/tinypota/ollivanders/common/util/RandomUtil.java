	public static String getRandomWordsFromUUID(UUID uuid, int amount) {
		Random random = new Random(uuid.getMostSignificantBits() ^ uuid.getLeastSignificantBits());
		
		StringBuilder stringBuilder = new StringBuilder();
		for (int i = 0; i < amount; i++) {
			if (i > 0) {
				stringBuilder.append(' ');
			}
			int randomIndex = random.nextInt(WORD_LIST.size());
			stringBuilder.append(WORD_LIST.get(randomIndex));
		}
		
		return stringBuilder.toString();
	}
